#include <iostream>
using namespace std;

int main()
{
    int a=4,b=5;
    // numerical operators
    cout<<"The value of a+b is: "<<a+b<<endl;
    cout<<"The value of a-b is: "<<a-b<<endl;
    cout<<"The value of a*b is: "<<a*b<<endl;
    cout<<"The value of a/b is: "<<a/b<<endl;
    cout<<"The value of a%b is: "<<a%b<<endl;
    cout<<"The value of a++ is: "<<a++<<endl;
    cout<<"The value of a-- is: "<<a--<<endl;
    cout<<"The value of ++a is: "<<++a<<endl;
    cout<<"The value of --a is: "<<--a<<endl;


    // comparison operators
    cout<<" The value of a== b is "<< (a==b)<<endl;
    cout<<" The value of a!= b is "<< (a!=b)<<endl;
    cout<<" The value of a>= b is "<< (a>=b)<<endl;
    cout<<" The value of a<= b is "<< (a<=b)<<endl;
    cout<<" The value of a> b is "<< (a>b)<<endl;
    cout<<" The value of a< b is "<< (a<b)<<endl;
    return 0;

}